// Get all city slugs for routing
export const getAllCitySlugs = () => SERVICE_CITIES.map(c => c.slug);

// Generate city-specific JSON-LD
export function generateCitySchema(city: CityPageData) {
  const yard = OPERATIONAL_YARDS.find(y => y.id === city.nearestYardId);
  return {
    "@context": "https://schema.org",
    "@type": "LocalBusiness",
    "@id": `${BUSINESS_INFO.url}/dumpster-rental/${city.slug}#business`,
    "name": `${BUSINESS_INFO.name} - ${city.name}`,
    "description": city.metaDescription,
    "telephone": BUSINESS_INFO.phone.sales,
    "email": BUSINESS_INFO.email,
    "url": `${BUSINESS_INFO.url}/dumpster-rental/${city.slug}`,
    "address": {
      "@type": "PostalAddress",
      "addressLocality": city.name,
      "addressRegion": city.state,
      "addressCountry": "US",
    },
    "geo": {
      "@type": "GeoCoordinates",
      "latitude": city.lat,
      "longitude": city.lng,
    },
    "areaServed": {
      "@type": "City",
      "name": city.name,
      "containedInPlace": {
        "@type": "AdministrativeArea",
        "name": city.county,
      },
    },
    "priceRange": "$$",
    "openingHoursSpecification": {
      "@type": "OpeningHoursSpecification",
      "dayOfWeek": ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"],
      "opens": "06:00",
      "closes": "21:00",
    },
    "hasOfferCatalog": {
      "@type": "OfferCatalog",
      "name": `Dumpster Rental in ${city.name}`,
      "itemListElement": DUMPSTER_SIZES_DATA.map(size => ({
        "@type": "Offer",
        "itemOffered": {
          "@type": "Service",
          "name": `${size.yards} Yard Dumpster Rental in ${city.name}`,
        },
        "priceSpecification": {
          "@type": "PriceSpecification",
          "price": size.priceFrom,
          "priceCurrency": "USD",
        },
      })),
    },
  };
}

// Generate sitemap entries for all cities
export function getCitySitemapEntries() {
  return SERVICE_CITIES.map(city => ({
    url: `/dumpster-rental/${city.slug}`,
    lastmod: new Date().toISOString().split('T')[0],
    priority: 0.8,
    changefreq: 'monthly' as const,
  }));
}
